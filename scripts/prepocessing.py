def detect_language(text):
    try:
        return detect(text)
    except:
        return "unknown"

def is_gibberish(text):
    if re.search(r"(.)\1{4,}", text):  # repeated chars
        return True
    if len(re.findall(r"[a-zA-Z]", text)) < 3:
        return True
    return False

print("ðŸŒ Detecting languages and filtering gibberish...")
